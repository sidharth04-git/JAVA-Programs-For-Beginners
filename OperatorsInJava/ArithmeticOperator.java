package OperatorsInJava;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int x = 10 , y = 5;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

    }
}
