package Conditionals;

public class IntAndFloatIsEqual {
    public static void main(String[] args) {
        int x = 3;
        double y = 3.0;
        if(x==y)
            System.out.println("x and y are Equal");
        else
            System.out.println("x and y are not Equal");
    }
}
